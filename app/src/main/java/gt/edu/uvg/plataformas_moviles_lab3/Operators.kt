package org.example

import kotlin.math.*

fun plus(num1: Double, num2: Double) = num1.plus(num2)

fun minus(num1 : Double, num2: Double) = num2.minus(num1)

fun div(num1: Double, num2: Double) = num2.div(num1)

fun multiply(num1: Double, num2: Double) = num1 * num2

fun power(num1: Double, num2: Double) = num2.pow(num1)

fun square(num1: Double) = sqrt(num1)
